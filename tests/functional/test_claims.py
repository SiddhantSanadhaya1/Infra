@pytest.mark.asyncio
class TestClaimStatusLifecycle:
    """Test claim status transitions (FR-2.2, FR-2.4)."""

    async def test_claim_status_values(self, client, active_policy, claim_factory):
        """
        FR-2.2.2: Test all status values: SUBMITTED, UNDER_REVIEW, APPROVED, REJECTED, PAID.
        """
        # Create claim
        payload = claim_factory(policy_id=active_policy["id"])
        create_response = await client.post("/api/claims", json=payload)
        assert create_response.status_code == 201
        claim_id = create_response.json()["id"]
        assert create_response.json()["status"] == "SUBMITTED"

        # SUBMITTED → UNDER_REVIEW
        update_response = await client.put(
            f"/api/claims/{claim_id}",
            json={"status": "UNDER_REVIEW"}
        )
        assert update_response.status_code == 200
        assert update_response.json()["status"] == "UNDER_REVIEW"

        # UNDER_REVIEW → APPROVED
        approve_response = await client.post(
            f"/api/claims/{claim_id}/approve",
            json={"amount_approved": 3000.00}
        )
        assert approve_response.status_code == 200
        assert approve_response.json()["status"] == "APPROVED"

        # APPROVED → PAID (manual update for testing)
        paid_response = await client.put(
            f"/api/claims/{claim_id}",
            json={"status": "PAID"}
        )
        assert paid_response.status_code == 200
        assert paid_response.json()["status"] == "PAID"


